let currentLang = DEFAULT_LANG;

// ==========================================
// Core Translation Function
// ==========================================

/**
 * Translate a key, with optional {param} interpolation.
 * Falls back to the default language (Tagalog), then to the raw key.
 *
 * @param {string} key
 * @param {Record<string, string|number>} [params]
 * @returns {string}
 */
export function t(key, params = {}) {
  const langData = translations[currentLang] || translations[DEFAULT_LANG];
  let str =
    langData?.[key] ??
    translations[DEFAULT_LANG]?.[key] ??
    key; // last resort: return the raw key

  // Replace {placeholder} tokens
  if (params && typeof params === "object") {
    for (const [k, v] of Object.entries(params)) {
      str = str.replace(new RegExp(`\\{${k}\\}`, "g"), v ?? "");
    }
  }

  return str;
}

// ==========================================
// Language Management
// ==========================================

/**
 * Return the current active language code.
 * @returns {string}
 */
export function getCurrentLang() {
  return currentLang;
}

/**
 * Switch to a different language and persist the choice.
 * Dispatches a "language-changed" CustomEvent on window.
 *
 * @param {string} lang - One of the keys in `translations`
 */
export function setLang(lang) {
  if (!translations[lang]) {
    console.warn(`[i18n] Unknown language: "${lang}". Valid options: ${Object.keys(translations).join(", ")}`);
    return;
  }
  currentLang = lang;
  localStorage.setItem(LANG_KEY, lang);
  applyTranslations();
  window.dispatchEvent(new CustomEvent("language-changed", { detail: { lang } }));
}

